 * \section descripcion Descripción General:
 * Este proyecto es una aplicación diseñada para facilitar la gestión de profesores, módulos y modelos creados por los profesores en el ámbito educativo. 
 * Su propósito es simplificar y agilizar el proceso de asignación de materias para el próximo curso académico. 
 * Ofrece una interfaz intuitiva y eficiente para que los profesores elijan las materias que desean impartir, mientras que el administrador puede gestionar la asignación de horarios y materias de manera centralizada.
 * 
 * \section funcionalidades Funcionalidades Principales:
 * - Gestión de usuarios (profesores y administradores).
 * - Creación y administración de módulos.
 * - Soporte CRUD (Crear, Leer, Actualizar, Eliminar) para módulos y modelos. Aunque por ahora solo C
 * - Interfaz de usuario intuitiva y fácil de usar.
 * - Implementación segura y escalable utilizando Docker.
 * 
 * \section estructura Estructura del Proyecto:
 * El proyecto está organizado en los siguientes módulos:
 *
 * - Backend: Contiene la lógica del servidor y las API REST.
 * - Frontend: Contiene la interfaz de usuario y las vistas correspondientes.
 * - Base de datos: Define los modelos de datos y las migraciones.
 * - Despliegue: Archivos y configuraciones para el despliegue utilizando Docker.
 * 
 * \section tecnologias Tecnologías Utilizadas:
 * - Lenguaje de programación: PHP
 * - Framework de frontend: react
 * - Base de datos: MySQL
 * - Contenedores: Docker
 * 
 * \section instalacion Instalación y Configuración:
 * 1. Clonar el repositorio: git clone https://github.com/sempereluismi/modular.git
 * 2. Navegar al directorio del proyecto: cd modular/server
 * 3. Construir y levantar los contenedores de Docker: docker compose up -d
 * 4. Acceder a la aplicación en el navegador en http://localhost:80.
